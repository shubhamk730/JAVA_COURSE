public class Literals {
    public static void main(String[] args) {
        // print binary number
        int num1 = 0b101;
        System.out.println(num1);

        // hexadecimal number
        int num2 = 0xf;
        System.out.println(num2);

        char c = 'a';
        c++;
        System.out.println(c - 0);
    }
}
