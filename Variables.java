public class Variables {
    public static void main(String[] args) {

        // integer variable
          int num1 = 5;
          int num2 = 9;
          System.out.println(num1 + num2);

          long l1 = 76555l;
          System.out.println(l1);

        // Data Types
        // primitive -> Integer(byte, short, int, long), float(double, float), character, boolean
        // Non primitive
        // float f1 = 5.7; // gives error
        float f1 = 5.7f;
        System.out.println(f1);

        char c = 'a';
        System.out.println(c);

        boolean b = false;
        System.out.println(b);

    }
}
