public class Loops {
    public static void main(String[] args) {

        int a = 5;

        while(a < 10) {
            System.out.println("Hello");
            a++;
        }

        do{
            System.out.println("Hello again");
        }while(a<10);

        for(int i = 0; i < 10; i++) {
            System.out.println("For loop");
        }

    }

}
